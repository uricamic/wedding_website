const langButtons = document.querySelectorAll("[data-lang-switch]");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const rsvpForm = document.getElementById("rsvpForm");
const formFeedback = document.getElementById("formFeedback");
const lightbox = document.getElementById("lightbox");
const lightboxImage = document.getElementById("lightboxImage");
const hasSupabaseSdk = typeof window.supabase !== "undefined";

const config = window.WEDDING_CONFIG || {};
const isSupabaseReady =
  Boolean(config.supabaseUrl) &&
  Boolean(config.supabaseAnonKey) &&
  hasSupabaseSdk;

let supabaseClient = null;
if (isSupabaseReady) {
  supabaseClient = window.supabase.createClient(
    config.supabaseUrl,
    config.supabaseAnonKey
  );
}

function applyTranslations(lang) {
  const dict = translations[lang] || translations.cs;

  document.documentElement.lang = lang;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (!dict[key]) {
      return;
    }

    if (node.tagName === "INPUT" || node.tagName === "TEXTAREA") {
      node.placeholder = dict[key];
      return;
    }

    if (node.tagName === "OPTION") {
      node.textContent = dict[key];
      return;
    }

    node.textContent = dict[key];
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.langSwitch === lang);
  });

  localStorage.setItem("wedding-lang", lang);
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyTranslations(button.dataset.langSwitch);
  });
});

const initialLang = localStorage.getItem("wedding-lang") || "cs";
applyTranslations(initialLang);

rsvpForm.addEventListener("submit", async (event) => {
  event.preventDefault();

  const formData = new FormData(rsvpForm);
  const data = Object.fromEntries(formData.entries());
  localStorage.setItem("rsvp-response", JSON.stringify(data));

  const lang = localStorage.getItem("wedding-lang") || "cs";
  formFeedback.textContent = translations[lang].rsvpSending;

  if (isSupabaseReady) {
    try {
      const payload = {
        name: String(data.name || ""),
        email: String(data.email || ""),
        phone: String(data.phone || ""),
        attendance: String(data.attendance || ""),
        guests: Number(data.guests || 0),
        accommodation: String(data.accommodation || ""),
        food: String(data.food || ""),
        message: String(data.message || "")
      };

      const { error } = await supabaseClient.from("rsvps").insert(payload);
      if (error) {
        throw error;
      }

      formFeedback.textContent = translations[lang].rsvpSuccess;
      rsvpForm.reset();
      return;
    } catch {
      formFeedback.textContent = translations[lang].rsvpError;
      return;
    }
  }

  if (window.location.protocol === "file:") {
    formFeedback.textContent = translations[lang].rsvpLocalSuccess;
    rsvpForm.reset();
    return;
  }

  const encodedData = new URLSearchParams();
  formData.forEach((value, key) => {
    encodedData.append(key, String(value));
  });

  try {
    await fetch("/", {
      method: "POST",
      headers: { "Content-Type": "application/x-www-form-urlencoded" },
      body: encodedData.toString()
    });
    formFeedback.textContent = translations[lang].rsvpSuccess;
    rsvpForm.reset();
  } catch {
    formFeedback.textContent = translations[lang].rsvpError;
  }
});

// Simple lightbox without external dependencies.
document.getElementById("galleryGrid").addEventListener("click", (event) => {
  const image = event.target.closest("img");
  if (!image) {
    return;
  }

  lightboxImage.src = image.src;
  lightboxImage.alt = image.alt;
  lightbox.showModal();
});

document.querySelector(".close-lightbox").addEventListener("click", () => {
  lightbox.close();
});

lightbox.addEventListener("click", (event) => {
  const bounds = lightboxImage.getBoundingClientRect();
  const inside =
    event.clientX >= bounds.left &&
    event.clientX <= bounds.right &&
    event.clientY >= bounds.top &&
    event.clientY <= bounds.bottom;

  if (!inside) {
    lightbox.close();
  }
});
